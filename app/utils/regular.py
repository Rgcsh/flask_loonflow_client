k_reg_date = r"^[1-9]\d{3}-(0[1-9]|1[0-2])-(0[1-9]|[1-2][0-9]|3[0-1])$"

# 时间正则 00:00:00
k_reg_time = r"^(20|21|22|23|[0-1]\d):[0-5]\d:[0-5]\d$"

# 日期+时间 正则 2019-11-12 00:00:00
